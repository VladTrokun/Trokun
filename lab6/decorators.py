# decorators.py

def require_nonempty(func):
    # Створюємо нову функцію-обгортку, яка буде виконувати перевірку перед викликом func
    def wrapper(*args, **kwargs):
        # 1. Перевіряємо звичайні аргументи (наприклад: "Влад")
        # 'args' - це набір (кортеж) усіх аргументів без імені
        for i, arg in enumerate(args):
            # 'if not arg' перевіряє на None, 0, "" (порожній рядок), [] (порожній список) тощо.
            if not arg:
                # Якщо знайшли порожній, одразу викликаємо помилку
                raise ValueError(f"Позиційний аргумент #{i} не може бути порожнім.")

        # 2. Перевіряємо іменовані аргументи (наприклад: age=20)
        # 'kwargs' - це словник усіх аргументів з іменем
        for key, value in kwargs.items():
            if not value:
                # Знайшли порожній іменований аргумент
                raise ValueError(f"Іменований аргумент '{key}' не може бути порожнім.")

        # Якщо всі перевірки пройшли (помилка не виникла)
        return func(*args, **kwargs)
    # Декоратор повертає цю нову функцію-обгортку
    return wrapper